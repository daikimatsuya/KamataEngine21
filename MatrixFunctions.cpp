Matrix4x4 MatrixFunctions::Transpose(const Matrix4x4& m) {
	Matrix4x4 tmp;
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			tmp.m[i][j] = m.m[j][i];
		}
	}
	return tmp;
}



Matrix4x4 MatrixFunctions::MakeIdentity() {
	Matrix4x4 tmp;
	for (int i = 0; i < 4; i++) {
		for (int j = 0; j < 4; j++) {
			tmp.m[i][j] = 0.0f;
			if ((i == 0 && j == 0) || (i == 1 && j == 1) || (i == 2 && j == 2) || (i == 3 && j == 3)) {
				tmp.m[i][j] = 1.0f;
			}
		}
	}
	return tmp;
}

